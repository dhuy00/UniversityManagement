-- Relational constraints

-- The two deferred constraints below resolve the intentional cycle:
-- UNITS.HEAD_STAFF_ID -> STAFF and STAFF.UNIT_ID -> UNITS.
-- A unit and its first staff/head records must be created in one transaction.
ALTER TABLE STAFF ADD CONSTRAINT FK_STAFF_UNIT
    FOREIGN KEY (UNIT_ID)
    REFERENCES UNITS (UNIT_ID)
    DEFERRABLE INITIALLY DEFERRED;

ALTER TABLE UNITS MODIFY (HEAD_STAFF_ID NOT NULL);

ALTER TABLE UNITS ADD CONSTRAINT FK_UNITS_HEAD_IN_UNIT
    FOREIGN KEY (HEAD_STAFF_ID, UNIT_ID)
    REFERENCES STAFF (STAFF_ID, UNIT_ID)
    DEFERRABLE INITIALLY DEFERRED;

ALTER TABLE COURSES ADD CONSTRAINT FK_COURSES_UNIT
    FOREIGN KEY (UNIT_ID)
    REFERENCES UNITS (UNIT_ID);

ALTER TABLE COURSE_PLANS ADD CONSTRAINT FK_COURSE_PLANS_COURSE
    FOREIGN KEY (COURSE_ID)
    REFERENCES COURSES (COURSE_ID);

ALTER TABLE TEACHING_ASSIGNMENTS ADD CONSTRAINT FK_ASSIGNMENTS_LECTURER
    FOREIGN KEY (LECTURER_ID)
    REFERENCES STAFF (STAFF_ID);

ALTER TABLE TEACHING_ASSIGNMENTS ADD CONSTRAINT FK_ASSIGNMENTS_COURSE_PLAN
    FOREIGN KEY (
        COURSE_ID,
        SEMESTER,
        ACADEMIC_YEAR,
        PROGRAM_ID
    )
    REFERENCES COURSE_PLANS (
        COURSE_ID,
        SEMESTER,
        ACADEMIC_YEAR,
        PROGRAM_ID
    );

ALTER TABLE ENROLLMENTS ADD CONSTRAINT FK_ENROLLMENTS_STUDENT
    FOREIGN KEY (STUDENT_ID)
    REFERENCES STUDENTS (STUDENT_ID);

ALTER TABLE ENROLLMENTS ADD CONSTRAINT FK_ENROLLMENTS_ASSIGNMENT
    FOREIGN KEY (
        LECTURER_ID,
        COURSE_ID,
        SEMESTER,
        ACADEMIC_YEAR,
        PROGRAM_ID
    )
    REFERENCES TEACHING_ASSIGNMENTS (
        LECTURER_ID,
        COURSE_ID,
        SEMESTER,
        ACADEMIC_YEAR,
        PROGRAM_ID
    );

