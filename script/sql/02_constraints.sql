-- Target database: Oracle Database 21c
-- Prerequisite: run 01_schema.sql as the application owner.

SET DEFINE OFF
WHENEVER SQLERROR EXIT FAILURE ROLLBACK

BEGIN
    IF SYS_CONTEXT('USERENV', 'SESSION_USER') IN ('SYS', 'SYSTEM') THEN
        RAISE_APPLICATION_ERROR(
            -20000,
            'Run application scripts as UNIVERSITY_APP, not SYS or SYSTEM.'
        );
    END IF;
END;
/

-- Unique constraints

ALTER TABLE UNITS ADD CONSTRAINT UQ_UNITS_NAME
    UNIQUE (UNIT_NAME);

ALTER TABLE STAFF ADD CONSTRAINT UQ_STAFF_ORACLE_USERNAME
    UNIQUE (ORACLE_USERNAME);

-- Required by the composite foreign key that guarantees a unit head belongs
-- to the same unit. STAFF_ID is already unique, but Oracle requires the exact
-- referenced column list to have a UNIQUE or PRIMARY KEY constraint.
ALTER TABLE STAFF ADD CONSTRAINT UQ_STAFF_ID_UNIT
    UNIQUE (STAFF_ID, UNIT_ID)
    DEFERRABLE INITIALLY DEFERRED;

ALTER TABLE STUDENTS ADD CONSTRAINT UQ_STUDENTS_ORACLE_USERNAME
    UNIQUE (ORACLE_USERNAME);

