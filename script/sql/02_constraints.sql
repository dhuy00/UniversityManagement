            (SEMESTER = 1 AND EXTRACT(MONTH FROM START_DATE) = 1)
            OR (SEMESTER = 2 AND EXTRACT(MONTH FROM START_DATE) = 5)
            OR (SEMESTER = 3 AND EXTRACT(MONTH FROM START_DATE) = 9)
        )
    );

